/**
 * 从 modood/Administrative-divisions-of-China 的 pc.json 生成地级城市清单，
 * 使用 pinyin-pro UMD 计算 name_pinyin（无声调，空格分词）与首字母（A–Z / #）。
 * 运行：node tools/generate-china-cities-sql.mjs
 * 输出：supabase/migrations/0008_china_cities_catalog.sql
 */
import fs from 'node:fs'
import https from 'node:https'
import path from 'node:path'
import vm from 'node:vm'
import { createRequire } from 'node:module'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')
const outSql = path.join(root, 'supabase', 'migrations', '0008_china_cities_catalog.sql')
const pinyinPath = path.join(root, 'web', '.tmp-pinyin.js')

function fetchText(url) {
  return new Promise((resolve, reject) => {
    https
      .get(url, (r) => {
        let d = ''
        r.on('data', (c) => (d += c))
        r.on('end', () => resolve(d))
      })
      .on('error', reject)
  })
}

function loadPinyin() {
  if (!fs.existsSync(pinyinPath)) {
    throw new Error(`缺少 ${pinyinPath}，请先下载 pinyin-pro UMD 到该路径`)
  }
  const code = fs.readFileSync(pinyinPath, 'utf8')
  const exp = {}
  const module = { exports: exp }
  const require = createRequire(import.meta.url)
  vm.runInNewContext(code, { module, exports: exp, console, require })
  return module.exports.pinyin
}

function sqlQuote(s) {
  return s.replace(/'/g, "''")
}

function initialLetter(pinyinStr) {
  const first = pinyinStr.trim().split(/\s+/)[0] || ''
  const ch = first[0]
  if (!ch) return '#'
  const up = ch.toUpperCase()
  if (up >= 'A' && up <= 'Z') return up
  return '#'
}

async function main() {
  const raw = await fetchText(
    'https://raw.githubusercontent.com/modood/Administrative-divisions-of-China/master/dist/pc.json',
  )
  const pc = JSON.parse(raw)
  const municipal = new Set(['北京市', '天津市', '上海市', '重庆市'])
  /** 直辖市在清单中单独成段时的固定顺序（与常见行政区划表述一致） */
  const municipalProvinceOrder = ['北京市', '天津市', '上海市', '重庆市']
  /** @type {{name:string;province:string}[]} */
  const rows = []
  for (const [prov, list] of Object.entries(pc)) {
    if (!Array.isArray(list)) continue
    if (municipal.has(prov)) {
      rows.push({ name: prov.replace(/市$/, ''), province: prov })
    } else {
      for (const c of list) rows.push({ name: c, province: prov })
    }
  }

  const pinyin = loadPinyin()
  const enriched = rows.map((r, i) => {
    const py = pinyin(r.name, { toneType: 'none', type: 'string' }).trim()
    const sec = initialLetter(
      py || pinyin(r.name, { pattern: 'normal', toneType: 'none', type: 'string' }),
    )
    const sortLetter = sec === '#' ? 'ZZZ' + r.name : sec
    return { ...r, name_pinyin: py, sortLetter, sort_order: 100 + i }
  })

  enriched.sort((a, b) =>
    a.sortLetter.localeCompare(b.sortLetter) ||
    a.name_pinyin.localeCompare(b.name_pinyin) ||
    a.name.localeCompare(b.name),
  )

  const isMunicipal = (r) => municipal.has(r.province)
  const muni = enriched.filter(isMunicipal).sort(
    (a, b) =>
      municipalProvinceOrder.indexOf(a.province) -
      municipalProvinceOrder.indexOf(b.province),
  )
  const rest = enriched.filter((r) => !isMunicipal(r))
  const ordered = [...muni, ...rest].map((r, idx) => ({
    ...r,
    sort_order: 100 + idx,
  }))

  /** 每行并排几条 value，减小迁移文件行数 */
  const TUPLES_PER_LINE = 4

  function tupleSql(r) {
    const name = sqlQuote(r.name)
    const prov = sqlQuote(r.province)
    const py = sqlQuote(r.name_pinyin.toLowerCase())
    return `('${name}', '${prov}', ${r.sort_order}, '${py}')`
  }

  /**
   * @param {{name:string;province:string;sort_order:number;name_pinyin:string}[]} slice
   */
  function pushChunkedValueLines(slice) {
    for (let i = 0; i < slice.length; i += TUPLES_PER_LINE) {
      const chunk = slice.slice(i, i + TUPLES_PER_LINE).map(tupleSql)
      const hasMoreInSlice = i + TUPLES_PER_LINE < slice.length
      lines.push(`    ${chunk.join(', ')}${hasMoreInSlice ? ',' : ''}`)
    }
  }

  const munisCount = muni.length
  const munisOrdered = ordered.slice(0, munisCount)
  const restOrdered = ordered.slice(munisCount)

  const lines = []
  lines.push(`-- Generated by tools/generate-china-cities-sql.mjs — DO NOT HAND EDIT`)
  lines.push(`alter table cities add column if not exists name_pinyin text;`)
  lines.push(``)
  lines.push(`create unique index if not exists cities_name_province_uidx`)
  lines.push(`  on cities (name, coalesce(province_name, ''));`)
  lines.push(``)
  lines.push(`merge into cities as c`)
  lines.push(`using (`)
  lines.push(`  values`)
  lines.push(`    -- 直辖市（4）`)
  pushChunkedValueLines(munisOrdered)

  if (restOrdered.length > 0) {
    lines[lines.length - 1] += ','
    lines.push(
      `    -- 省 / 自治区 / 特别行政区等（其余 ${restOrdered.length} 条；每行约 ${TUPLES_PER_LINE} 条）`,
    )
    pushChunkedValueLines(restOrdered)
  }

  lines.push(`) as v(name, province_name, sort_order, name_pinyin)`)
  lines.push(`on c.name = v.name and coalesce(c.province_name, '') = coalesce(v.province_name, '')`)
  lines.push(`when matched then`)
  lines.push(
    `  update set sort_order = v.sort_order, name_pinyin = v.name_pinyin, is_active = true`,
  )
  lines.push(`when not matched then`)
  lines.push(
    `  insert (name, province_name, sort_order, name_pinyin, is_active)`,
  )
  lines.push(`  values (v.name, v.province_name, v.sort_order, v.name_pinyin, true);`)

  fs.writeFileSync(outSql, lines.join('\n') + '\n', 'utf8')
  console.log('written', outSql, 'rows', ordered.length)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
